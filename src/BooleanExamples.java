
public class BooleanExamples {

	public static void main(String[] args) {
		
		//A boolena is any variable or parameter with a TRUE of FALSE
		boolean isDriving =false;
		
		String name = "car";
		//a conditional statement IF Else
		if(isDriving) {
			System.out.println("William is driving his "+name);
		}else {
			System.out.println("William is not driving his "+name);
		}
		
		
		

	}

}
